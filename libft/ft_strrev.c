#include "libft.h"

void	ft_strrev(char *str)
{
	char	s1;
	char	*s2;

	s2 = str + ft_strlen(str) - 1;
	while (str < s2)
	{
		s1 = *str;
		*str++ = *s2;
		*s2-- = s1;
	}
}
