#include "libft.h"

char	*ft_strtoupper(char *str)
{
	char	*res;

	res = str;
	while (*str != '\0')
	{
		if (*str <= 'z' && *str >= 'a')
			*str = *str - 32;
		str++;
	}
	return (res);
}
