#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *cs1;

	cs1 = s1;
	while (*cs1 != '\0')
		cs1++;
	while (*s2 != '\0')
		*cs1++ = *s2++;
	*cs1 = '\0';
	return (s1);
}
